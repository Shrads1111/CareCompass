const I18n = {
    currentLang: localStorage.getItem('app-lang') || 'en',

    setLanguage: function (lang) {
        if (!translations[lang]) return;
        this.currentLang = lang;
        localStorage.setItem('app-lang', lang);
        this.translatePage();
        this.updateSelect();
    },

    translatePage: function () {
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(el => {
            const key = el.getAttribute('data-i18n');
            const placeholderKey = el.getAttribute('data-i18n-placeholder');

            if (key && translations[this.currentLang][key]) {
                el.innerText = translations[this.currentLang][key];
            }
            if (placeholderKey && translations[this.currentLang][placeholderKey]) {
                el.placeholder = translations[this.currentLang][placeholderKey];
            }
        });

        document.documentElement.lang = this.currentLang;
    },

    getVoiceLang: function () {
        switch (this.currentLang) {
            case 'hi': return 'hi-IN';
            case 'mr': return 'mr-IN';
            default: return 'en-US';
        }
    },

    renderLanguageSelector: function (containerId) {
        const container = document.getElementById(containerId);
        if (!container) return;

        // Clear existing content to prevent duplicates/stale state
        container.innerHTML = '';

        const wrapper = document.createElement('div');
        wrapper.className = 'lang-switcher';

        ['en', 'hi', 'mr'].forEach(lang => {
            const btn = document.createElement('button');
            btn.className = `lang-btn ${this.currentLang === lang ? 'active' : ''}`;
            btn.setAttribute('data-lang', lang);

            // Use localized name for the button text
            // We use the 'currentLang' translation for the name so "Hindi" appears as "à¤¹à¤¿à¤‚à¤¦à¥€" 
            // ONLY if the current lang is Hindi? 
            // OR: Standard practice to show language names in their OWN language always?
            // User asked "each word on screen should change".
            // So I will use the translation key for the language name from the *current* language dictionary.
            // This means if I am in English, I see "Hindi". If in Hindi, I see "à¤¹à¤¿à¤‚à¤¦à¥€".
            btn.innerText = translations[this.currentLang][lang === 'en' ? 'english' : (lang === 'hi' ? 'hindi' : 'marathi')];

            btn.onclick = () => this.setLanguage(lang);
            wrapper.appendChild(btn);
        });

        container.appendChild(wrapper);
    },

    updateSelect: function () {
        // Update all language switchers on the page
        const metaButtons = document.querySelectorAll('.lang-btn');
        metaButtons.forEach(btn => {
            // Update active state
            if (btn.getAttribute('data-lang') === this.currentLang) {
                btn.classList.add('active');
            } else {
                btn.classList.remove('active');
            }

            // Update text to match current language selection
            const langCode = btn.getAttribute('data-lang');
            const key = langCode === 'en' ? 'english' : (langCode === 'hi' ? 'hindi' : 'marathi');
            btn.innerText = translations[this.currentLang][key];
        });
    },

    init: function () {
        // Wait for DOM
        if (document.readyState === 'loading') {
            document.addEventListener('DOMContentLoaded', () => this.translatePage());
        } else {
            this.translatePage();
        }
    }
};

// Auto-init
I18n.init();
